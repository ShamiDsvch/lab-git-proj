public class Utils
{ 
    public int multiply_numbers(int n, int m)
    {
        return n * m;
    }
    public int add_numbers(int n, int m)
    {
        return n + m;
    }
    public double divide_numbers(int n, int m) 
    { 
        return n / m;
    }
    public int subtract_numbers(int n, int m)
    {
        return n - m;
    }
}